namespace AhuErp.Core.Models
{
    /// <summary>
    /// Направление документа в делопроизводстве (входящий / исходящий /
    /// внутренний / распорядительный). Используется для журналов регистрации
    /// и формирования регистрационного номера.
    /// </summary>
    public enum DocumentDirection
    {
        /// <summary>Внутренний (служебная записка, докладная и т.п.).</summary>
        Internal = 0,

        /// <summary>Входящий (поступивший от внешнего корреспондента).</summary>
        Incoming = 1,

        /// <summary>Исходящий (направляемый внешнему адресату).</summary>
        Outgoing = 2,

        /// <summary>Распорядительный (приказ, распоряжение).</summary>
        Directive = 3
    }
}
